package zoomapi.components.componentResponseData;

public class Message {
    String id;
    String message;
    String sender;
    String date_time;
    String timestamp;

    public String getId() {
        return id;
    }

    public String getMessage() {
        return message;
    }

    public String getSender() {
        return sender;
    }

    public String getDate_time() {
        return date_time;
    }

    public String getTimestamp() {
        return timestamp;
    }
}
